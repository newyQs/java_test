package java_base;

public class comparative_algorithm {
    public static void main(String[] args) {
        int num1 = 4;
        int num2 = 5;
        System.out.println("num1>num2返回的值为：" + (num1 > num2));
        System.out.println("num1>=num2返回的值为：" + (num1 >= num2));
        System.out.println("num1<num2返回的值为：" + (num1 < num2));
        System.out.println("num1<=num2返回的值为：" + (num1 <= num2));
        System.out.println("num1==num2返回的值为：" + (num1 == num2));
        System.out.println("num1!=num2返回的值为：" + (num1 != num2));
    }
}
